#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*dup;
	char	*str;

	str = (char *)s;
	i = ft_strlen(s);
	if (!(dup = (char *)malloc(sizeof(char) * i + 1)))
		return (NULL);
	i = 0;
	while (str[i] != '\0')
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
